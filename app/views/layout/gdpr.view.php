<hr>

<h3>2. Vilka personuppgifter behandlas?</h3>
<p>
  Budget-appen behandlar endast de uppgifter som krävs för att tillhandahålla
  tjänsten. Följande uppgifter kan behandlas:
</p>

<ul>
  <li>Namn (valfritt)</li>
  <li>E-postadress (för inloggning och kommunikation)</li>
  <li>Inkomster och utgifter som användaren själv registrerar</li>
  <li>Budgetposter och kategorier</li>
  <li>Importerade transaktioner (om funktionen används)</li>
</ul>

<p>
  All ekonomisk information som registreras i tjänsten är endast tillgänglig
  för respektive användare.
</p>

<hr>

<h3>3. Ändamål med behandlingen</h3>
<p>Personuppgifterna används för att:</p>

<ul>
  <li>Möjliggöra inloggning och användarkonto</li>
  <li>Visa och sammanställa användarens budget, inkomster och utgifter</li>
  <li>Ge en översikt över faktisk kostnad jämfört med budget</li>
  <li>Möjliggöra import av transaktioner (om tillämpligt)</li>
  <li>Kommunicera viktig information om tjänsten</li>
</ul>

<hr>

<h3>4. Rättslig grund</h3>
<p>
  Behandlingen av personuppgifter sker med stöd av:
</p>

<ul>
  <li><strong>Avtal</strong> – för att kunna tillhandahålla budgettjänsten</li>
  <li><strong>Samtycke</strong> – för eventuell e-postkommunikation eller valfria funktioner</li>
  <li><strong>Berättigat intresse</strong> – för drift, säkerhet och förbättring av tjänsten</li>
</ul>

<hr>

<h3>5. Lagringstid</h3>
<p>
  Personuppgifter sparas så länge användarkontot är aktivt.
  Användaren kan när som helst begära att kontot och tillhörande uppgifter raderas.
</p>

<p>
  Backupkopior kan förekomma under en begränsad tid av tekniska skäl.
</p>

<hr>

<h3>6. Dina rättigheter</h3>
<p>Du har rätt att:</p>

<ul>
  <li>Begära ett registerutdrag över vilka uppgifter som behandlas</li>
  <li>Begära rättelse av felaktiga uppgifter</li>
  <li>Begära radering av dina personuppgifter</li>
  <li>Invända mot eller begränsa behandlingen</li>
  <li>Få dina uppgifter exporterade (dataportabilitet)</li>
</ul>

