<h1>Integritetspolicy (GDPR)</h1>

<p>
  Denna integritetspolicy beskriver hur personuppgifter hanteras på
  <strong>budget.kpb.nu</strong>, i enlighet med EU:s dataskyddsförordning (GDPR).
</p>

